    //Hash password
    public static String hashPassword(String password) throws Exception {
        MessageDigest md = MessageDigest.getInstance("SHA-256");
        byte[] hash = md.digest(password.getBytes());
        StringBuilder hexString = new StringBuilder();
        for (byte b : hash) {
            String hex = Integer.toHexString(0xff & b);
            if (hex.length() == 1) hexString.append('0');
            hexString.append(hex);
        }
        return hexString.toString();
    }
    
    public void authenticateUser() {
        id = usernameField.getText();
        String password = new String (passwordField.getPassword());
        
        String hashed_password = null;
        try {
            hashed_password = hashPassword(password);
        } catch (Exception ex) {
            Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        //database connection
        if (Database.authenticate(id, hashed_password)) {
            JOptionPane.showMessageDialog(this, "Login Successful!");
            this.dispose(); //close login window
            
            String role = Database.getRole(id, hashed_password);
            
            // Open the dashboard based on the role
            if ("staff".equals(role)) {
                StaffDashboard staffDashboard = new StaffDashboard(id); //open staff dashboard
            }
            else {
                StudentDashboard studentDashboard = new StudentDashboard(id); //open student dashboard
            }            
        }
        else {
            JOptionPane.showMessageDialog(this, "Please check your credentials!",  "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

