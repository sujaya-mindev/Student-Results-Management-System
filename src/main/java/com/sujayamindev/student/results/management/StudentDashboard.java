    private void courseComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_courseComboBoxActionPerformed
        courseId = (String) courseComboBox.getSelectedItem(); // Retrieves the selected course ID from the combo box.
        
        // Clear all records
        DefaultTableModel model = (DefaultTableModel) resultTable.getModel();   
        model.setRowCount(0); 
             
        // Get results
        try {
            if (!"---".equals(courseId)) {  // Checks if a valid course ID is selected (not the placeholder ---).
                Database db = new Database();   // Calls a method from the Database class to get the student's results for the selected course.
                List<String> results = db.getResultsStudent(id, courseId);

                if (results.isEmpty()) {    // Checks if there are any results returned.
                    statusTextField1.setText("No results available!");   // Displays a message if no results are available.
                } else {
                    // Set table header text bold
                    JTableHeader tableHeader = resultTable.getTableHeader();    // Gets the header of the resultTable.
                    Font headerFont = new Font("Segoe UI", Font.BOLD, 13);  // Sets the font of the table header to a bold Segoe UI font.
                    tableHeader.setFont(headerFont);

                    // Set Table Columns Width
                    TableColumnModel columnModel = resultTable.getColumnModel();	// Retrieves the column model of the table to set individual column widths.
                    columnModel.getColumn(0).setPreferredWidth(90); // Module 
                    columnModel.getColumn(1).setPreferredWidth(15); // Exam
                    columnModel.getColumn(2).setPreferredWidth(15); // CW
                    columnModel.getColumn(3).setPreferredWidth(15); // Grade
                    columnModel.getColumn(4).setPreferredWidth(15); // GP

                    // Center align table cells
                    DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
                    centerRenderer.setHorizontalAlignment(JLabel.CENTER);
                    int nocols = model.getColumnCount();
                    
                    // Add results to the table
                    for(int x=0;x<nocols;x++){
                        resultTable.getColumnModel().getColumn(x).setCellRenderer( centerRenderer );
                    }

                    int rowSize = 5; // Defines the number of elements in each row (module, exam marks, coursework marks, grade, grade points).
                    for (int i = 0; i < results.size(); i += rowSize) { // Loops through the results list in chunks of 5 elements.
                        Object[] rowData = new Object[rowSize];
                        for (int j = 0; j < rowSize; j++) {
                            rowData[j] = results.get(i + j);
                        }
                        model.addRow(rowData);  // Add data to the table
                    }
                }
            } else {
                statusTextField1.setText("");
            }
        }
        catch (Exception e) {
        }
    }//GEN-LAST:event_courseComboBoxActionPerformed

    private void themeToggleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themeToggleButtonActionPerformed
        ImageIcon lightIcon = new ImageIcon(getClass().getResource("/icons/light-mode.png"));
        ImageIcon darkIcon = new ImageIcon(getClass().getResource("/icons/dark-mode.png"));

        if (themeToggleButton.isSelected()) {
            FlatDarkLaf.setup();
            SwingUtilities.updateComponentTreeUI(this);
            themeToggleButton.setIcon(darkIcon);
        } else {
            FlatLightLaf.setup();
            SwingUtilities.updateComponentTreeUI(this);
            themeToggleButton.setIcon(lightIcon);
        }
    }//GEN-LAST:event_themeToggleButtonActionPerformed

